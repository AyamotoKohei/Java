public class Operators{
    public static void main(String[] args){
        Integer value01 = 2;
        Integer value02 = 4;
        System.out.printf("%d + %d= %d%n", value01, value02, value01 + value02);
        System.out.printf("%d - %d= %d%n", value01, value02, value01 - value02);
        System.out.printf("%d * %d= %d%n", value01, value02, value01 * value02);
        System.out.printf("%d / %d= %d%n", value01, value02, value01 / value02);
        System.out.printf("%d %% %d= %d%n", value01, value02, value01 % value02);
    }
}